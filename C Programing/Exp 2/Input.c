// This Program have a basic overview of scanf statement and variables taken 
#include <stdio.h>
int main()
{
  int age;
  printf("Enter your age: "); // This statement is used to print the statement written in the double quotes
  scanf("%d", &age); // This statement is used to take input from the user
  printf("Your age is %d", age);
  return 0;
  
  
}